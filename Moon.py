from time import sleep
from userbot.events import register


@register(outgoing=True, pattern='^.fl(?: |$)(.*)')
async def typewriter(typew):
    await typew.edit("`start loading...`")
    sleep(1)
    await typew.edit("🌝")
    sleep(0.03)
    await typew.edit("🌚")
    sleep(0.03)
    await typew.edit("🌝")
    sleep(0.03)
    await typew.edit("🌚")
    sleep(0.03)
    await typew.edit("🌝")
    sleep(0.03)
    await typew.edit("🌚")
    sleep(0.03)
    await typew.edit("🌝")
    sleep(0.03)
    await typew.edit("🌚")
    sleep(0.03)
    await typew.edit("🌝")
    sleep(0.03)
    await typew.edit("🌚")
    sleep(0.03)
    await typew.edit("🌝")
 
    sleep(1)
    await typew.edit("`Done!`")
